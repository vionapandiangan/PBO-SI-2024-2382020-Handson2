public class UnaryOperator {
    public static void main(String[]args) {
        int x = 10;
        // pre-Increment
        System.out.println(++x);
        // post-Increment
        System.out.println(x++);

        int y = 10;
        // pre-Decrement
        System.out.println(--y);
        //post-Decrement
        System.out.println(y--);

        // Logical NOT
        boolean isTrue = true;
        System.out.println(isTrue);
    }
}
